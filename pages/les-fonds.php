			<h2 style="margin-bottom: 12px;"><span>Fonds Pierre-Olivier Walzer</span></h2>

	       <br/>
			 <strong>Dans une lettre de 1945, Cingria loue « l'activité angélique » déployée à son endroit par Pierre-Olivier Walzer, son voisin du 5 de la Grand-Rue à Fribourg: le Fonds P.-O. Walzer, créé aux ALS en 1991, vient brillamment confirmer son sentiment.</strong>
			<br/><br/>Walzer y a rassemblé un bel ensemble de correspondances et manuscrits, qui va de ce qui est probablement la plus ancienne pièce de Cingria, « A la fille du Payien »,  aux manuscrit et épreuves du Novellino, titre qui paraîtra en édition posthume en 1955, en passant par un exemplaire des Autobiographies de Brunon Pomposo enrichi de corrections et ajouts de la main de Cingria.<br/><br/>On y trouve les grands manuscrits du Pétrarque, du Camp de César, des Florides helvètes (dont Walzer fut l'éditeur aux Portes de France), mais aussi les petits textes polémiques qui fustigent le monde éditorial en 1944 (« Auteurs et éditeurs », « Tarifs de misère ») ainsi que l'amusante participation de Cingria aux publicités de Radio-Luxembourg (« Indicatif Philips »), le répertoire d'adresses utilisé par Charles-Albert dans lequel il notait à la volée des titres qui intriguent encore et, les manuscrits de quelques textes isolés  : « A propos de la séparation de l'église et de l'état », « La Leçon de la Villa d'Este » ou « La Folle Chevauchée ».
			Bibliophile, Walzer a collectionné pratiquement tous les titres de Cingria (jusqu'au rarissime Espéranto) en édition originale sur grand papier, ainsi que de nombreux numéros de ces revues réputées introuvables, dans lesquelles Cingria aimait à disperser ses titres.<br/><br/>
			Si Walzer à bien évidemment conservé les lettres que lui adressaient Cingria, il leur a adjoint, dans sa collection, les missives de Cingria reçues par des amis intimes, citons Abdul Wahab ou  Pierre Jaquet, ainsi que des pièces isolées adressées à Cocteau, Gagnebin ou Pierre Minet.<br/><br/>
			Un important second pan des archives Walzer témoigne de la réception des écrits de Cingria pour laquelle il aura tant œuvré. On y trouve aussi bien les travaux préparatoires de la première édition des <a href="/oeuvres_completes" class="inline_link"><span>Œuvres complètes</span></a> et de la correspondance générale, que la documentation de L'Association des Amis de Charles-Albert Cingria alors qu'il la présidait ou les maquettes des <a href="/petites_feuilles" class="inline_link"><span>Petites Feuilles</span></a> pour les numéros qu'il préparait avec Jean-Louis Kuffer. Mais aussi la documentation et la préparation des très nombreux articles, monographies, conférences, soirées ou expositions que Walzer consacra à Cingria et qui, à présent, forme une excellente source pour la recherche.

