    <h1><span>Les Petites Feuilles</span></h1>

     <p>  <br/>
     <strong><em>Les Petites Feuilles</em> ont été fondées en novembre 1941 par Géa Augsbourg, Cingria  et Paul Budry et se définissaient dans leur sous-titre : «le plus petit tirage du plus petit journal paraissant irrégulièrement à St-Saphorin ».</strong>  <br/><br/>Elles n'étaient l'organe d'aucune autre collectivité que celle des amis habitués de l'Auberge de l'Onde, à Saint-Saphorin, et présentaient, illustrées, des nouvelles, de la critique littéraire, artistique et musicale, des horoscopes, des annonces, bref, un tas de choses. Elles ne connurent que cinq numéros : leur publication fut interrompue au moment de la prise d’Ambositra par les troupes britanniques, le 16 octobre 1942.

     <br/><br/>
       Près de cinquante années plus tard, au cours de l’assemblée générale de l’Association des Amis de Charles-Albert Cingria, du 16 novembre 1991, Pierre-Olivier Walzer proposa leur relance pour assurer la création d’une publication régulière qui serait un organe de liaison entre l’Association et ses membres. La proposition fut adoptée et  les Petites Feuilles relancées l’année suivante  avec le numéro 6.
    <br/><br/>
       Sous la direction de Jean-Louis Kuffer, à Lausanne, P.-O. Walzer étant le rédacteur en chef à Berne, les nouvelles Petites Feuilles présentèrent, au rythme de deux fois par an, maints inédits de Cingria, des reprises de témoignages oubliés et des contributions originales de Jacques Chessex, Jacques Réda, Maurice Chappaz, Gérard Macé, Pierre Voélin, Georges Arès, Frédéric Wandelère… Ainsi que diverses nouvelles autour de Cingria et de notre Association.
    <br/><br/>
    Arrivés en 2000 au numéro 20, P.-O. Walzer et J.-L. Kuffer ont passé la main à Marie-Thérèse Lathion. La passation de pouvoir et la remise des archives ont eu lieu le vendredi 24 novembre 2000. Le surlendemain, les Petites Feuilles furent endeuillées par le soudain décès du professeur Walzer. Néanmoins, nous avons maintenu le cap de nos devanciers en publiant un bon nombre de textes et de lettres inédits de Cingria, de documents, d'iconographie et d'informations le concernant de près ou de loin, et en ajoutant aux auteurs déjà nommés Michel Butor, Marie Gaulis, Alain Corbellari, François Blocquaux, Doris Jakubec.

