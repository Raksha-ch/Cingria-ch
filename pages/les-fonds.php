		 <h1><span>Charles-Albert Cingria à la BCU </span></h1>
		<p>
		<br/>


		<strong>La Bibliothèque cantonale et universitaire de Lausanne conserve des manuscrits et dactylogrammes dans le fonds Charles-Albert Cingria, constitué à partir de 1987 par des achats successifs. On y trouve notamment un exemplaire corrigé de <em>Pendeloques alpestres</em>, une dactylographie avec corrections manuscrites de <em>Bois sec Bois vert</em>, un manuscrit partiel d’<em>Enveloppes</em>, celui de <em>La Reine Berthe et sa famille</em> et celui de <em>Notre terre et ses gens</em>.</strong> <br/><br/>
		D’autres documents appartiennent à des archives éditoriales. Celles de la Guilde du livre, confiées à la BCU en 1981, contiennent plusieurs textes qui accompagnaient des lettres de l’écrivain à Albert Mermoud, le directeur de l’enseigne à laquelle ont paru Notre terre et ses gens et Stalactites. C’est le cas aussi du fonds Charles Veillon, donné par ce dernier en 1957, qui garde la trace de textes publiés dans le journal de l’entreprise lausannoise de confection.<br/><br/>
		Signalons encore des pages manuscrites dans les fonds Paul Budry, Daniel Simond et C. F. Ramuz, ainsi que, dans ce dernier fonds, des lettres de Cingria à Ramuz. Enfin, la BCU possède également un important ensemble de lettres adressées par Cingria à Meraud Guevara-Guiness.<br/><br/><br/>
