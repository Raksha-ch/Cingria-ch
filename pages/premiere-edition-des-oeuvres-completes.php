			   <h1><span>Les <em>Propos</em></span></h1>
				<div id="main_oc_2">
					<p>
			   		<strong>A la différence des Récits, les écrits de ces nouveaux volumes, souvent brefs, ne racontent pas une ou des histoires, mais ils renseignent ou convainquent sur « toutes sortes de choses ».</strong>
						<br/><br/>Selon leur thème dominant, ils sont répartis en huit sections : Esthétique générale, Poétique, Littérature et Beaux-Arts pour le premier volume, Musique, Idées politiques, Philosophie et Religion, enfin Culture et Société pour le second. Dans ces quelque six cents textes, dont la moitié environ avait été laissée inédite par l’auteur, chacun trouvera ses propres sujets de prédilection, et les idées de Cingria bien affirmées, avec humeur ou délicatesse, enthousiasme ou mépris. Deux cahiers iconographiques agrémentent ces textes.
						<br/><br/>La remarquable culture de Cingria et ses sauts dans le temps et dans l’espace, associés à une spontanéité toujours en éveil, font de ces articles, originairement disséminés dans de multiples revues et aujourd’hui rassemblés, un délice de lecture. Ne boudez pas ce plaisir…
			    </div>
						
			    </p>
			     <br/><br/>
			   <h1><span>Les <em>Essais</em></span></h1>
				<div id="main_oc_2">
					<p>
<strong>Réunissant tout ce qui ressortit à la posture « érudite » de Cingria, les deux volumes des Essais forment véritablement le cœur de son œuvre.</strong> <br/><br/>Car l’érudition est loin d’être un passe-temps accidentel de notre auteur : elle est motivée par des aspirations profondes qui tendent à situer dans le Moyen Age central un apogée de la culture et de la civilisation occidentale que Cingria n’aura de cesse d’exalter et de faire redécouvrir. <br/><br/>Portés par un désir de réhabilitation, ces deux volumes ont été les plus difficiles à mettre en œuvre. Les sujets abordés par Cingria n’étant pas forcément familiers à tous les lecteurs, l’explication et le balisage annoté de son travail se sont avérés indispensables, tout comme un travail éditorial approfondi sur les portées dont l’auteur parsème ses textes musicologiques. <br/><br/>Le premier volume des Essais contient des études sur la musique du Moyen Age et la civilisation de Saint-Gall, ainsi qu’un ensemble de textes sur la Pré-Renaissance italienne. Le second volume contient un vaste ensemble inédit sur la poésie courtoise, intitulé <em>Les Grands Lyriques provençaux cités par Dante et par Pétrarque</em>, ainsi que des textes historiques majeurs comme <em>La Reine Berthe et sa famille</em>. 			    </div>

			
			
				
			
			
			
